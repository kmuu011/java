package test22;

public class WhileTest {
	
	public static void main(String[] args) {
		
		int i=1;
		while(i<10) {
			System.out.println(i);
			i++;
		}
		
		System.out.println("----------------");
		
		int j=1;
		
		do {
			System.out.println(j++);
		} while(j<10);
		
		
		
		
		
		
	}

}
