package test26.animal;

public class Animal {

	private String name;

	public Animal(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	public String toString() {
		return "Animal [name=" + name + "]";
	}
	
	
	

}
